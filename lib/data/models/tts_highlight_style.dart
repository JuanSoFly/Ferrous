enum TtsHighlightStyle {
  /// Soft background pill (default) - no borders, soft round corners, theme-matched text.
  softPill,
  /// Sleek underline - no background fill, just a modern primary-colored line below active words.
  underline,
  /// Classic Clean - clean outline and soft primary fill (replacing the old harsh yellow/orange neon).
  classicClean,
}
